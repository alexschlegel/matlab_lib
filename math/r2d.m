function d = r2d(r)
% r2d
% 
% Description:	convert an angle from radians to degrees
% 
% Syntax:	d = r2d(r)
% 
% In:
% 	r	- the angle, in radians
% 
% Out:
% 	d	- the angle, in degrees
% 
% Updated:	2008-11-10
% Copyright 2008 Alex Schlegel (schlegel@gmail.com).  All Rights Reserved.
d	= r*180/pi;
